/// Enum to define the platform style.
///
/// [material] - Use Material Design style.
/// [cupertino] - Use Cupertino (iOS) style.
enum PlatformStyle { material, cupertino }

/// Enum to specify the button position.
///
/// [normal] - Default position of the button.
/// [bottom] - Button is positioned at the bottom.
/// [top] - Button is positioned at the top.
enum ButtonPosition { normal, bottom, top }

/// Enum to define the dropdown mode.
///
/// [normal] - Default dropdown mode.
/// [sheet] - Dropdown displayed as a sheet.
enum DropdownMode { normal, sheet }
